# ------------ Arithmatic Operators ------------
a = 10
b = 5

addition = a + b
subtraction = a - b
multiplication = a * b
division = a / b
modulo = a % b

print("addition: ", addition)
print("subtraction: ", subtraction)
print("multiplication: ", multiplication)
print("division: ", division)
print("modulo: ", modulo)

# ------------ Comparison Operators ------------
x = 10
y = 20

equal_to = x == y
not_equal_to = x != y

print("equal_to: ", equal_to)
print("not_equal_to: ", not_equal_to)

# ------------ Logical Operators ------------
p = True
q = False

logical_and = p and q
logical_or = p or q
logical_not = not p

print("logical_and: ", logical_and)
print("logical_or: ", logical_or)
print("logical_not: ", logical_not)

